""" Number operations """
print(2+2)  # 4

print(10 - 1) # 9

print(10 * 2) # 20

print(10 / 2) # 5.0

print(5 ** 3) # 125, its a power

print(11 // 2) # 2, its a ceiling value of division

print(11 % 2)  # 1, modulas


""" Math funcions """

print(round(2.50))  # 2, ceiling
print(round(2.51))  # 3, floor

print(abs(-21)) # 21, absoutue value give positive num



""" Binary & Decimal conversation """

print(bin(5)) # 0b101, D->B , 0b define binary number start

print(int('0b101', 2)) # 5, 2 is binary base value
