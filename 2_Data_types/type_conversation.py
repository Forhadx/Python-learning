# Data type 

print(type(2)) # <class 'int'>
print(type(2.11)) # <class 'float'>
print(type('hello')) # <class 'str'>
print(type(True)) # <class 'bool'>
print(type([1,3,4])) # <class 'list'>



# boolean operation

print(bool(True))   # True
print(bool(False))   # False

print(2 + bool(True))   # 3

print(3 * bool(False))  # 0



